      <h2>GENERAL</h2>
      <div>
        <h2>Can I smoke in the theatre?</h2>
        <p>Smoking, consumption of alcohol, chewing paan and spitting are strictly prohibited within the theatre premises as per government order.</p>
        <h2>What do I do about lost property?</h2>
        <p>In case of lost belongings, please contact one of our staff members for help. They will find out if anything has been turned in. Management will not be responsible for any loss or damage of personal belongings.</p>
        <h2>Can I take photographs and video in the theatre?</h2>
        <p>For security purposes we do not permit any form of photography or videography within the screens.  However, you are welcome to take pictures in our lobbies and other outdoor areas.  Any violation will be subject to legal action.</p>
        <h2>How can I get a job with SPI Cinemas?</h2>
        <p>Please check our career section or send in your CV to <a href="mailto:jobs.chn@spicinemas.in">jobs.chn@spicinemas.in</a> for Chennai and <a href="mailto:jobs.cbe@spicinemas.in">jobs.cbe@spicinemas</a> for Coimbatore.</p>
        <h2>Do you cater for birthday parties or functions/catering for large groups?</h2>
        <p>Yes. Please write to our customer service representative at <a href="mailto:events.chn@spicinemas.in">events.chn@spicinemas.in</a> with your requirements and someone from our team will contact you.</p>
