        <p>Wheelchair facility is available on request.  You may contact our security personnel for the same.  We will also provide any other assistance you may require in terms of accessibility for seniors or physically challenged customers.</p>
        <h2>I can't find an FAQ to answer my question, what can I do?</h2>
        <p>Please contact one of our staff members in person or write to us at <a href="mailto:help@spicinemas.in">help@spicinemas.in</a> or call 044-4224 4224 to speak with a customer care representative.</p>
