      <h2>PRE-BOOK</h2>
      <div>
        <h2>What is Pre-Booking?</h2>
        <p>This new feature allows you to Pre-Book your tickets and get in queue to reserve your seats much before regular online bookings open.</p>
        <h2>How do I Pre-Book tickets?</h2>
        <p>You can Pre-Book tickets for movies listed under coming soon with the Pre-Booking option. You can select your preferences of cinema, showtimes, date and experiences. Allocation of your tickets will happen based on the preferences you give us!</p>
        <h2>Does Pre-Booking guarantee my tickets?</h2>
        <p>There is no guarantee offered as Pre-Booking works on a system of allocation on a first-come first served basis.</p>
        <h2>Can I pick my seats when I Pre-Book?</h2>
        <p>Seating of Pre-Booked tickets will be as allotted by SPI Cinemas at the time of exchange of the Pre-Booked tickets.</p>
        <h2>What happens if my Pre-Booking is not allotted?</h2>
        <p>The money received by SPI Cinemas in exchange of the Pre-Booked tickets shall be held by SPI Cinemas with no interest and shall be refunded within 7 - 10 working days.</p>
        <h2>What happens if my choice of movie is postponed?</h2>
        <p>For movie releases that are postponed, refunds will be initiated automatically.</p>
        <h2>Am I eligible for a refund if I cancel my Pre-Booked ticket?</h2>
        <p>Currently, cancellation is non-refundable.</p>
        <h2>How do I check my Pre-Booking details once I receive a confirmation?</h2>
        <p>Under Pre-Booking history, you will find your booking ID. To know more about your booking details, you can check your regular order history.</p>
        <h2>My money has been debited but I haven’t received a confirmation. What do I do now? </h2>
        <p>Be sure to check your Pre-Booking order history. If the transaction is seen in the order history, your Pre-Booking request has been received. You will get a confirmation shortly thereafter informing you if you have been allotted your seats or not. In the event that your transaction is not seen in the Pre Booking order history, send us an email with your problem to <a href="mailto:help@spicinemas.in">help@spicinemas.in</a>.</p>
        <h2>Is there a Pre-Booking charge?</h2>
        <p>A regular booking fee of INR 30 will be charged along with other applicable charges such as 3D wherever appropriate.</p>
