      <h2>FOOD &amp; BEVERAGE</h2>
      <div>
        <h2>Can I order my food online?</h2>
        <p>Yes.  During your booking process you will be provided with an option of ordering food online.  Simply select your choice of food and beverage and you can have them delivered to your seats during the intermission.</p>
        <h2>Is there an additional charge for ordering food online?</h2>
        <p>Yes.  An additional charge of Rs.10/- will be charged for the entire transaction.</p>
        <h2>Can I order more food from the steward?</h2>
        <p>Sorry.  This facility is presently unavailable.  Only previously ordered items will be served by the steward.  You can always pick up more food at our Concession Counters.</p>
        <h2>Can I cancel my order?</h2>
        <p>Sorry.  This facility is presently unavailable.</p>
        <h2>Can I bring my own food to the theatre?</h2>
        <p>As per company policy, we do not permit outside food for security reasons.</p>
