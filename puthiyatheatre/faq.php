      <h2>TICKETS</h2>
      <div>
        <h2>How can I book tickets at SPI Cinemas?</h2>
        <p>You can book your tickets at the theatre, online through the website, <a href="https://www.spicinemas.in/">www.spicinemas.in</a> or by calling 044-4224 4224 for home delivery of tickets.</p>
        <h2>Can I book my tickets without logging into my account?</h2>
        <p>You need to login to book tickets. You can sign up at <a href="https://www.spicinemas.in/">www.spicinemas.in</a> to book movie tickets online.</p>
        <p>&nbsp;</p>
        <h2>Where do I pick up my movie tickets?</h2>
        <p>You do not require a ticket to enter the screen! All you need to do is show the SMS or email confirmation of your ticket booking to the attendant at the screen entry of your chosen film. In the rare event that you do not receive your SMS or email confirmation, you can retrieve your booking number from the order history and collect your tickets by referencing the booking number at the ticket counter at the respective site.</p>
        <h2>What is my booking confirmation number?</h2>
        <p>It is a reference number that is displayed on your screen after the successful completion of your online booking.</p>
        <h2>Where can I get my booking confirmation number?</h2>
        <p>Once your booking is successful, the booking confirmation number will be displayed on your screen.  An email will also be sent to your registered email address for reference.  Alternatively, you can login to your account and click on Order History.</p>
        <h2>What do I do if I do not get a booking confirmation number or am unsure if my booking is done after the payment process is complete?</h2>
        <p>Simply re-login and check your order history to confirm whether the tickets have been booked.  If your booking doesn't reflect here, your tickets have not been booked and the payment has not been processed, in which case you need to redo the booking.</p>
        <h2>Can I buy movie tickets for future dates?</h2>
        <p>Absolutely!  We feature advance ticket sales on this website.  The number of days in advance that you can buy tickets for a film varies by date.  Generally, a theater's release schedule for a movie runs from Friday to Thursday.  Show times for this theater generally appear on Wednesday (Tuesday at midnight).</p>
        <h2>Is it OK to buy tickets from any person on the premises?</h2>
        <p>Tickets may be purchased only at the ticket counter, call center or this website.  Purchasing tickets from any unauthorized person is illegal and punishable.</p>
        <h2>Can I cancel my tickets online?</h2>
        <p>Tickets once purchased cannot be cancelled or exchanged for an alternative show.  You could request our staff an hour before the movie to assist you in reselling the tickets.</p>
        <h2>Can I sell the tickets to someone else?</h2>
        <p>Unauthorized selling of tickets on the theatre premises is illegal and punishable.  If you are seen selling tickets on the premises, it could be perceived as illegal, even if your intentions were innocent.</p>
        <h2>I missed my show. Can I use the same ticket for another show?</h2>
        <p>A ticket purchased for a particular show is valid only for that show. It is advisable to purchase another ticket for another show of your choice.</p>
        <h2>What do I do if I lose my tickets ?</h2>
        <p>Please keep your tickets carefully. We will not be able to help you in the case of lost tickets, as we do not maintain records of individual customers to whom tickets are sold.</p>
        <h2>Do children require tickets?</h2>
        <p>Children aged 3 years and above require a ticket to enter.</p>
        <h2>Can I book tickets online using Movie Vouchers?</h2>
        <p>Vouchers can be used to purchase tickets at the counters only. We regret any inconvenience caused.</p>
        <h2>I have been charged more than once for my online booking. How do I get a refund?</h2>
        <p>If you think you have been double charged, please first check your Order History to confirm the same. If the double booking is not reflected, the extra charge will automatically be credited back into your account within 7-10 working days.</p>
        <p>For any further assistance, please email <a href="mailto:help@spicinemas.in">help@spicinemas.in</a> with the cinema location, movie title and session time, and your best contact number or call 044-4224 4224 to speak with a customer care representative.</p>
        <h2>Is there a booking fee to book tickets online?</h2>
        <p>Yes. A charge of Rs. 30 will be levied per ticket when you book online.</p>
        <h2>Is there an extra fee for 3D films?</h2>
        <p>Yes.  A fee of Rs. 30 will be charged per ticket for the 3D experience.</p>
        <h2>How do I make a bulk booking request?</h2>
        <p>For tickets of 20 or more, please email <a href="mailto:groups.chn@spicinemas.in">groups.chn@spicinemas.in</a> for Chennai and <a href="mailto:groups.cbe@spicinemas.in">groups.cbe@spicinemas.in</a> for Coimbatore.</p>
