      <h2>CENSOR RATING</h2>
      <div>
        <h2>What are censor ratings?</h2>
        <p>Ratings provided by the Central Board of Film Certification which rate suitability of each film are called censor ratings.</p>
        <p>U: Unrestricted Public Exhibition throughout India</p>
        <p>A: Public exhibition restricted to Adults above 18 years only</p>
        <p>U/A: Unrestricted public exhibition with parental guidance for children below the age of 12</p>
        <p>V/U: Sanctioned for video exhibition and given a rating of Unrestricted Public Exhibition</p>
        <p>S: Exhibition to restricted audience such as doctors</p>
        <h2>Are children below 4 years of age permitted into the cinema halls?</h2>
        <p>We do not recommend bringing children below the age of 4 to the cinema hall.  Their admission is subject to the censor rating of the film.  Children below the age of 18 will not be permitted into the hall for A rated movies under any circumstances.*</p>
        <p>*If you have children with you when you arrive at the hall at our Sathyam Cinemas location, you can leave them at 'Magic Hat', our child care facility, for the duration of the movie.  We have professionally trained personnel who will take care of the children while you enjoy the movie.  Contact Customer Care for more information on Magic Hat.  THIS FACILITY IS AVAILABLE AT SATHYAM CINEMAS ONLY AND NOT AT ALL LOCATIONS.</p>
