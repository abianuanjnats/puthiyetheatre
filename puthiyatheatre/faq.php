      <h2>FUEL WALLET</h2>
      <div>
        <h2>What is the Fuel wallet?</h2>
        <p>Fuel has moved to a digital space. Every customer now has their own Fuel wallet. You can top up this wallet with any amount you desire and transact at the touch of a button using the SPI cinemas mobile application available on iOS and Android and your registered Fuel mobile number</p>
        <h2>What are the benefits of the Fuel wallet?</h2>
        <p>In our continuing efforts to enhance movie-going experiences, the Fuel wallet makes your experience more convenient and secure than ever before. No hassles of cash, cards, pin and payment gateways. Everything can be done from the convenience of your smartphone through the SPI Cinemas Mobile Application available on iOS &amp; Android as well as <a href="https://www.spicinemas.in/">www.spicinemas.in</a>. Whats more? Your wallet can be used for bookings over the phone through 4224 4224 - our in-house call centre or over the counter at any of our theatres.</p>
        <h2>How do I recharge my Fuel wallet?</h2>
        <p>Recharging your Fuel wallet is very simple.</p>
        <p><strong>SPI Cinemas App:</strong> <br />
          1. On the top right corner of the screen, you will find the menu button. Tap the menu button.<br />
          2. Once the menu opens, you will find the ‘My wallet’ option.<br />
          3. Tap the “My wallet’ option and follow the on-screen instructions.</p>
        <p><strong>www.spicinemas.in:</strong><br />
          1. Log into your SPI Cinemas profile.<br />
          2. Click on Your profile on the top right corner of the screen.<br />
          3. Select the ‘Fuel wallet’ option.<br />
          4. Follow the on-screen instructions.</p>
        <h2>How do I transact with my Fuel wallet?</h2>
        <p>The uniqueness of our ‘Fuel wallet’ is the ability to use it at all Points of Sale.</p>
        <p>You may use your wallet for purchases at SPI Cinemas in the following ways.<br />
            <br />
          SPI Cinemas Mobile Application &amp; <a href="https://www.spicinemas.in/">www.spicinemas.in</a>:<br />
          1. Proceed with your transaction until the food selection page. <br />
          2. At the bottom of the food selection page, you will find 2 options - Instant pay with Fuel &amp; Payment Options.<br />
          3. Tap the ‘Instant Pay with Fuel wallet’ option. Thats it, your done! <br />
          Tip: We enable this button only if you have sufficient balance in your wallet.</p>
        <p>At the Call Centre - 4224 4224:<br />
          1. If you dial in to 4224 4224 from your registered mobile number, you wallet will be pre-authenticated and charged.<br />
          2. If you dial in from another number, provide the agent your registered phone number and we will text or call you with the OTP. Provide the OTP to our staff and your done!</p>
        <p>Over the Counter with a smart phone:<br />
          1. Walk up to the counter<br />
          2. Provide the Customer Executive with the OTP found in your Fuel dashboard. This is located under the “Fuel wallet” menu on the mobile application as well as your mobile friendly website (<a href="https://www.spicinemas.in/">www.spicinemas.in</a>). <br />
          3. Provide the last three digits of your registered mobile number and our staff will complete the transaction.</p>
        <p>Over the counter without a smart-phone or data:<br />
          1. If you do not have a smart phone, provide the Customer executive with your registered mobile number, email ID or wallet ID<br />
          2.  You will receive the OTP by sms or through a call.<br />
          3. Give the Customer Executive the OTP and our staff  will complete your transaction.</p>
        <p>Kiosk:<br />
          1. Coming Soon!</p>
        <p>Tip: Bring your registered mobile for a seamless booking experience.</p>
        <h2>How do I migrate my balance?</h2>
        <p>We have taken to liberty to auto migrate all accounts that have the same mobile number registered on the old Fuel card platform as well as on www.spicinemas.in. If you find your account and balance has not been automatically migrated, you will be able to do this at the payment stage during your purchase on the mobile app or on our website. All you need is your Fuel card number and your Fuel PIN.</p>
        <p>Alternatively, you can approach any of the counters at any of the theatres where our staff will gladly help you.</p>
        <p>You may also dial into 4224 4224 where one of our agents will assist you in migrating your balance.</p>
        <h2>How do refunds work on my Fuel wallet?</h2>
        <p>Refunds are applicable for Show cancellations and non allotment of tickets for Prebooking.</p>
        <p>For show cancellations, if you have purchased your tickets online or through the apps and paid via Credit Card/Netbanking/Fuel wallet your money will be refunded instantly back in your Fuel wallet for your convenience.</p>
        <h2>How secure is my Fuel wallet?</h2>
        <p>All transactions made through the Fuel wallet are encrypted and secure as per RBI regulations and standards. </p>
        <p>Further we have industry standard encryption to secure your login credentials. We have an additional factor of authentication through your mobile phone to ensure that your money is always safe with you. </p>
        <h2>Can I use other modes of payment if I do not have access to my wallet?</h2>
        <p>You will still be able to use all other payment modes at SPI Cinemas. We recommend the Fuel wallet for  seamless and secure transactions.</p>
        <h2>Can I make a partial payment through my Fuel wallet?</h2>
        <p>Can I make a partial payment through my Fuel wallet?<br />
          We will intimate you when we enable this feature for the SPI Cinemas mobile app, <a href="https://www.spicinemas.in/">www.spicinemas.in</a> &amp; our Call centre.</p>
        <h2>Can I withdraw the balance from my Fuel wallet if I don’t want to use it on the wallet?</h2>
        <p>In line with RBI directives money once recharged into the Fuel wallet cannot be withdrawn.</p>
        <h2>Can I pay for other services such as Flip, Bliss &amp; Parking with my Fuel wallet?</h2>
        <p>Yes you can pay at Flip and Bliss using the Fuel wallet. </p>
        <p>Parking at Sathyam and at the malls where our cinemas are located do  not accept Fuel wallet, only cash payments.</p>
        <h2>I’m not interested in using the wallet and would like my money back. What do I do?</h2>
        <p>If you do not wish to migrate your balance to the Fuel wallet we will gladly issue a refund of the amount you have in your Fuel account.</p>
        <h2>Is it possible to have 2 wallets with the same mobile number even if my IDs are different?</h2>
        <p>Each customer’s wallet is linked to their SPI Cinemas account. An SPI Cinemas account can have only one Mobile number registered with it. </p>
        <p>Registering your mobile number on another SPI Cinemas account will deactivate the previous SPI Cinemas account leaving the associated wallet unusable.</p>
        <h2>Is there a minimum or maximum balance requirement for the Fuel wallet?</h2>
        <p>There is no minimum balance requirement for the Fuel wallet. The maximum balance on the Fuel wallet can be Rs 5,000.</p>
        <h2>I have not received my OTP. What do I do?</h2>
        <p>You do not require an OTP for <a href="https://www.spicinemas.in/">www.spicinemas.in</a> &amp; the SPI Cinemas mobile app.</p>
        <p>If you face this problem at the ticket &amp; food counters or at the Call Centre, we will call you on the registered mobile number and speak out the OTP</p>
        <p>Tip: Please keep your registered mobile handy as you will not be able to complete your transaction without it.</p>
        <h2>Money has been debited but I haven’t received my tickets. What do I do?</h2>
        <p>Check your order history located in your profile section of the app or website. If the booking can be seen in your order history, your booking is confirmed.</p>
        <p>If the booking doesn’t reflect, the amount will be refunded to your wallet in 24 working hours.</p>
